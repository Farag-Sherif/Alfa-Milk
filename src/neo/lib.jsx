export const isArabic = (lang) => lang === "ar";
export const t = (lang, key, fallback = "") =>
  dictionary[lang]?.[key] ?? dictionary.ar[key] ?? fallback ?? key;

export const pickTranslation = (translations = [], lang = "ar", field = "name") => {
  if (!Array.isArray(translations)) return "";
  const locale = isArabic(lang) ? "ar" : "en";
  const hit = translations.find((entry) => entry?.locale === locale);
  if (hit?.[field]) return hit[field];
  return hit?.name || translations[0]?.[field] || translations[0]?.name || "";
};

export const stripHtml = (html = "") =>
  String(html || "").replace(/<[^>]+>/g, " ").replace(/\s+/g, " ").trim();

export const productName = (product, lang = "ar") =>
  pickTranslation(product?.translations, lang, "name") || product?.name || "Product";

export const categoryName = (category, lang = "ar") =>
  pickTranslation(category?.translations, lang, "name") || category?.name || "";

export const productOrigin = (product, lang = "ar") =>
  pickTranslation(product?.translations, lang, "country_origin") || product?.country_origin || "—";

export const productWeight = (product, lang = "ar") =>
  pickTranslation(product?.translations, lang, "weight") || product?.weight || "—";

export const parseVariations = (variations) => {
  if (!variations) return [];
  try {
    const parsed = typeof variations === "string" ? JSON.parse(variations) : variations;
    return Array.isArray(parsed) ? parsed : [];
  } catch (error) {
    return [];
  }
};

export const getBasePrice = (product) => {
  const variations = parseVariations(product?.variations);
  if (variations.length > 0) {
    const prices = variations.map((v) => Number(v?.price) || 0).filter(Boolean);
    if (prices.length) return Math.min(...prices);
  }
  return Number(product?.price) || 0;
};

export const getFinalPrice = (product, rawPrice) => {
  const price = rawPrice ?? getBasePrice(product);
  const discount = Number(product?.discount) || 0;
  if (!price) return 0;
  return discount > 0 ? price - price * (discount / 100) : price;
};

export const formatWeight = (weight, lang = "ar") => {
  if (!weight) return "";
  const str = String(weight);
  if (isArabic(lang)) return str;
  return str.replace(/كجم/g, "kg").replace(/جم/g, "g");
};

export const formatCurrency = (value, lang = "ar") => {
  const numeric = Number(value) || 0;
  return new Intl.NumberFormat(isArabic(lang) ? "ar-EG" : "en-US", {
    style: "currency",
    currency: "EGP",
    maximumFractionDigits: 0
  }).format(numeric);
};

export const formatDate = (value, lang = "ar") => {
  if (!value) return "";
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return "";
  return date.toLocaleDateString(isArabic(lang) ? "ar-EG" : "en-US", {
    year: "numeric",
    month: "short",
    day: "numeric"
  });
};

export const getCartQty = (item) => Number(item?.pivot?.qty || item?.qty || 1);

export const createCartItemFromProduct = (product, variation) => {
  if (!variation) return product;
  return {
    ...product,
    cartItemId: `${product.id}-${variation.weight}`,
    selectedProductSize: variation.weight,
    selectedVariation: variation,
    size: variation.weight,
    weight: variation.weight,
    price: Number(variation.price) || 0
  };
};

export const getOrderItems = (order) => order?.items || order?.order_items || order?.products || [];

export const getOrderStatus = (order, lang = "ar") => {
  return pickTranslation(order?.status?.translations, lang, "name") || order?.status?.name || order?.status || "—";
};

export const calculateCartTotal = (cartItems = []) =>
  (Array.isArray(cartItems) ? cartItems : []).reduce((sum, item) => {
    const unit = getFinalPrice(item, Number(item?.price) || getBasePrice(item));
    return sum + unit * getCartQty(item);
  }, 0);

export const StarRow = ({ rating = 0 }) => {
  const full = Math.round(Number(rating) || 0);
  return (
    <span className="ng-star-row" aria-label={`rating-${full}`}>
      {[1, 2, 3, 4, 5].map((value) => (
        <span key={value} className={value <= full ? "filled" : ""}>★</span>
      ))}
    </span>
  );
};
